// Loop for que itera de 0 a 9
for (let i = 0; i < 10; i++) {
    console.log('Repetindo for :>> ', i); // Exibe o valor atual de i em cada iteração
}

// Inicializa um array com alguns números
let arr = [1, 2, 3, 4];

// Loop for que itera sobre os índices do array arr
for (let j = 0; j < arr.length; j++) {
    console.log('Imprimindo os valores do arr :>> ', arr[j]); // Exibe o valor atual do array
}

// Exibe o comprimento do array arr
console.log('Usando o length :>> ', arr.length); // Exibe 4, que é o número de elementos no array

// Loop for que começa em 5 e multiplica x por 3 a cada iteração
for (let x = 5; x < 100; x *= 3) {
    console.log('Valor de x :>> ', x); // Exibe o valor atual de x em cada iteração
}
