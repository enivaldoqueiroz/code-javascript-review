let x = 1;
let y = 2;

console.log('Valor de x :>> ', x);
console.log('Valor de y :>> ', y);

// Soma
console.log('Soma: x = x + y :>> ', x = x + y);
console.log('Subtração: x += y :>> ', x += y);

// Subtração
console.log('Subtração: x -= y :>> ', x -= y);

// Multiplicação
console.log('Multiplicação: x *= y :>> ', x *= y);

// Divisão
console.log('Divisão: x /= y :>> ', x /= y);

// Loops
console.log('Loops: x++ :>> ', x++);
console.log('Loops: x-- :>> ', x--);

while (x <= 100) {
    console.log('x :>> ', x);
    x *= 2;
}
