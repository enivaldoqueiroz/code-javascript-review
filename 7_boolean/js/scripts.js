// Declaração de uma variável booleana com o valor 'true'
var verdadeiro = true;

console.log(verdadeiro); // Exibe o valor da variável 'verdadeiro' (true)
console.log(typeof verdadeiro); // Exibe o tipo de 'verdadeiro' (boolean)

// Exibição direta do tipo de 'true' e 'false'
console.log('typeof true :>> ', typeof true); // Exibe o tipo de 'true' (boolean)
console.log('typeof false :>> ', typeof false); // Exibe o tipo de 'false' (boolean)

// Declaração de uma variável booleana com o valor 'false'
var falso = false;

console.log('falso :>> ', falso); // Exibe o valor da variável 'falso' (false)
console.log('typeof falso :>> ', typeof falso); // Exibe o tipo de 'falso' (boolean)
