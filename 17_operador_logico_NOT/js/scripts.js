// A condição é verdadeira (true)
if (true) {
    // Este bloco será executado, pois a condição é verdadeira
    console.log('É verdadeiro :>> ', true); // Exibe "É verdadeiro :>> true"
}

// A condição é a negação de false (!false), que é verdadeira
if (!false) {
    // Este bloco também será executado, pois !false é verdadeiro
    console.log('É verdadeiro :>> ', !false); // Exibe "É verdadeiro :>> true"
}
